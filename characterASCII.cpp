#include <iostream>
using namespace std;


int main()
{
	char letter;

	letter = 65;
	cout << "The number 65 prints the letter: " << letter << endl;

	letter = 66;
	cout << "The number 66 prints the letter: " << letter << endl;

	return 0;
}

